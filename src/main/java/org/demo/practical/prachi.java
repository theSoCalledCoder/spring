package org.demo.practical;

public class prachi {
    private String s;

    public prachi(String s) {
        this.s = s;
    }

    public prachi() {
    }

    @Override
    public String toString() {
        return "prachi{" +
                "s='" + s + '\'' +
                '}';
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }
}
